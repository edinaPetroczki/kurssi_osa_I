                <h3>Pystypiano</h3>
                <p>Pystypiano – jota kutsutaan vain nimellä piano, jos sekaannuksen vaaraa ei ole – on yleinen koti- ja koulusoitin. Rakenne on kompakti ja tilantarve vähäinen. Pystypiano on yleensä hinnaltaan halvempi kuin flyygeli. Kielet ovat pystysuorassa ja koneisto on soittajasta katsoen kielten edessä. Kaikupohja on vastaavasti kielten takana. Vasarat liikkuvat likimain vaakasuorassa ja palautuvat lepoasentoonsa koneiston jousien voimasta. Pianon koon mitta on sen korkeus; tällä hetkellä valmistettavien mallien korkeus on yleensä 109–132 cm. Yleensä katsotaan pianon korkeuden kasvaessa saavutettavan etuja äänen laadussa ja kantavuudessa. Suomessa ei ole tapana kutsua pystypianoja niiden korkeuden mukaan eri nimillä, toisin kuin anglo-amerikkalaisessa maailmassa.</p>
            </div>
            <div class="column">
                <h3>Flyygeli</h3>
                <p>Flyygelin nimitys tulee soittimen muodosta, joka muistuttaa siipeä (saks. der Flügel 'siipi'). Flyygelin kielet ovat vaakatasossa, kaikupohja on niiden alla ja vasarat lyövät kieliin altapäin. Vasarat palautuvat lepoasentoon painovoiman vaikutuksesta, eli säädettäviä ja kuoleutuvia jousia ei ole. Flyygelin koneistossa on ominaisuus, joka mahdollistaa saman sävelen nopean toistamisen eli repetition. Flyygelin kielet ovat yleensä pidemmät kuin pystypianon kielet ja kaikupohja on suurempi. Konserttisoittimena käytetään yksinomaan flyygeliä. Flyygeli on myös ammattilaisen ja vaativan harrastajan soitin, sillä flyygelin koneisto on nopeampi kuin perinteisen pystypianon. Flyygeli on kuitenkin tilaa vievä ja usein hyvin kallis. Pienimmät flyygelit ovat noin 150 cm pitkiä. Konserttiflyygelinä yleisen Steinway & Sonsin D-mallin pituus on 274 cm ja paino 480 kg. Tämän kokoista flyygeliä kutsutaan täysmittaiseksi.</p>
