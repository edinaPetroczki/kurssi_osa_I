            <p>Piano on kosketinsoitin, jossa ääni syntyy huopapäällysteisten vasaroiden lyödessä soittimen sisälle jännitettyjä metallikieliä. Kielten värähtely siirtyy puiseen kaikupohjaan, joka vahvistaa ja värittää ääntä sekä muuttaa värähtelyn ilman liikkeeksi, joka aistitaan äänenä. Vasaroiden liikettä hallitaan koskettimistolta. Näkökulman mukaan pianoa voidaan pitää kielisoittimena, kosketinsoittimena tai jopa lyömäsoittimena. Sachs-Hornbostel-soitinluokituksen mukaan piano on kordofoni eli kielisoitin, tarkemmin sanottuna laatikkositra, jossa on koskettimisto. Pianolla on tärkeä asema länsimaisessa, erityisesti klassisessa musiikissa soolo- ja säestyssoittimena, kamarimusiikissa sekä suosittuna työkaluna säveltämisessä, musiikkiesitysten harjoittamisessa ja musiikin opiskelussa. Piano on tärkeä soitin myös populaarimusiikissa. Piano on yleinen kotisoitin.</p>
